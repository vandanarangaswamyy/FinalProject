    private void populateCombo() {

        
        String entType = String.valueOf(enterprise.getEnterpriseType());
        System.out.println(enterprise.getEnterpriseType());
        cmbOrgType.removeAllItems();

        if (entType.equals("Housing")) {
            cmbOrgType.addItem(Organization.Type.TempHousing);
            cmbOrgType.addItem(Organization.Type.PermHousing);
        }

        if (entType.equals("Hospital")) {
            cmbOrgType.addItem(Organization.Type.MentalHeathCare);
            cmbOrgType.addItem(Organization.Type.PhysicalHealthCare);
        }

        if (entType.equals("Education")) {
            cmbOrgType.addItem(Organization.Type.VocationalSchool);
            cmbOrgType.addItem(Organization.Type.GradSchool);
        }
          if (entType.equals("Job")) {
            cmbOrgType.addItem(Organization.Type.Job);
        }
    }
    


    private void populateTable(){
        
        DefaultTableModel model = (DefaultTableModel) tblOrg.getModel();
        
        
        model.setRowCount(0);
        
        for (Organization organization : directory.getOrganizationList()){
            
            Object[] row = new Object[2];
            
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }

